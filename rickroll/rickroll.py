def roll(no_fullscreen = False, no_keep_on_top = False):
    root = tk.Tk()

    # imposta un titolo per la finestra
    root.title('Sei stato rickrollato')

    # Imposta lo sfondo nero
    root.configure(bg="black")

    # Rimuovi la cornice
    # root.overrideredirect(True)

    if no_fullscreen:
        # Imposta le dimensioni della finestra su 500x500 pixel
        root.geometry("500x500")

        # Ottieni le dimensioni dello schermo
        screen_width = root.winfo_screenwidth()
        screen_height = root.winfo_screenheight()

        # Calcola la posizione iniziale per centrare la finestra
        x_position = (screen_width - 500) // 2
        y_position = (screen_height - 500) // 2

        # Imposta la posizione della finestra
        root.geometry(f"500x500+{x_position}+{y_position}")


         # Massimizza la finestra
        root.attributes('-zoomed', True)
    
    else:
        # Rendi la finestra a schermo intero
        root.attributes('-fullscreen', True)



    # Definisci un font monospace
    monospace_font = ("Courier", 14)  # Puoi personalizzare la dimensione del font

    # Aggiunge una label alla finestra con testo bianco, font monospace e allineamento in basso
    message = tk.Label(root, text="", bg="black", fg="white", font=monospace_font, anchor="s")
    message.pack(side="bottom", fill="both", expand=True)


    index = 0

    def change_text():
        nonlocal index  # Per accedere alla variabile index esterna

        # Cambia il testo della label
        message.config(text=rickRoll[index])

        # Incrementa l'indice (cicla all'inizio se raggiunge la fine)
        index = (index + 1) % len(rickRoll)

        # Pianifica la successiva modifica dopo 33 millisecondi
        root.after(36, change_text) # GIF real duration [s]: 4.91 x 146 frame

    # Pianifica la prima modifica dopo 2000 secondi
    root.after(2000, change_text)



    def keep_on_top():
        # Mantieni la finestra in primo piano
        root.lift()
        # Pianifica l'azione successiva dopo 100 millisecondi (puoi regolare il valore a seconda delle tue esigenze)
        root.after(10, keep_on_top)

    if not no_keep_on_top:
        root.after(10, keep_on_top)

    if not no_fullscreen:
        # Rendi la finestra a schermo intero
        root.attributes('-fullscreen', True)


    def volUP():
      keyboard = Controller()

      keyboard.press(Key.media_volume_up)
      time.sleep(0.02)
      keyboard.release(Key.media_volume_up)
      time.sleep(0.02) 


    def start_in_thread_volUP():
      volUP_thread = multiprocessing.Process(target=volUP)

      # Avviare il processo
      volUP_thread.start()

      # Attendere che il processo termini
      volUP_thread.join()

      root.after(10, start_in_thread_volUP)


    root.after(10, start_in_thread_volUP)

    # Mantiene la finestra visibile
    root.mainloop()
